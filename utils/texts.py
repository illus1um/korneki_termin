def get_text(key: str, lang: str = 'kk', **kwargs) -> str:
    """
    –ü–æ–ª—É—á–∏—Ç—å —Ç–µ–∫—Å—Ç –ø–æ –∫–ª—é—á—É –Ω–∞ —É–∫–∞–∑–∞–Ω–Ω–æ–º —è–∑—ã–∫–µ
    
    Args:
        key: –ö–ª—é—á —Ç–µ–∫—Å—Ç–∞
        lang: –Ø–∑—ã–∫ ('kk' –∏–ª–∏ 'ru')
        **kwargs: –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, count=5)
        
    Returns:
        –û—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç
    """
    texts = TEXTS.get(lang, TEXTS['kk'])
    text = texts.get(key, f'[{key}]')
    
    if kwargs:
        try:
            return text.format(**kwargs)
        except KeyError:
            return text
    
    return text

